<p>
    A francia partok közelében a Csatorna-szigetek között a Nagy-Britanniához tartozó kis sziget,
    Jersey jelentős szerepet játszott a világ szarvasmarha-tenyésztésében.
    Óceáni, enyhe, csapadékos éghajlata kedvezően hatott a szarvasmarha-állományok tejelékenységére.
    A kiváló legelőkön már a 13. sz. elején kiváló tenyésztőmunkát folytattak.
    A 18.sz. végén a sziget fajtáját - akkor még alderney néven - kiváló és zsírdús tejéről ismerték.
    Jersey szigeten 1789-tól hosszú ideig zárt tenyésztést folytattak, élő szarvasmarhát nem volt szabad sem behozni, sem kivinni.
    A szigeten viszonylag kis populációkban folyt a fajtatiszta tenyésztés, ezáltal egyöntetű küllemű és kiváló tulajdonságú állományok jöttek létre.
    1866-ban megalakult a Jersey Tenyésztő Szövetség, s munkájuk eredményeként 1912-től teljesítményvizsgálatot is végeztek.
</p>
