    Sajátos utat követtek a dán tenyésztők, akik már 1896-ban importáltak a fajtából.
    A küllemi jellegvonásokkal alig törődtek, így a dán jersey kevésbé nemes, valamivel nagyobb testű,
    mint az eredeti fajtaváltozat, tejtermelése ugyanakkor kiváló.
