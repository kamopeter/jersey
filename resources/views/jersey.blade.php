    Csukafejalakulás, nagy kissé dülledt szemek, finom szarvak jellemzik.
    A Jersey szigeti változat igen nemes, kistestű és finom csontú.
    Ezek az állatok őzekre emlékeztető kecsességűek. Valamivel erőteljesebb,
    de még mindig nemes megjelenésű az USA, valamint Kanada jersey marhája.
    Bizonyos mértékben különleges helyet foglal el az új-zélandi jersey,
